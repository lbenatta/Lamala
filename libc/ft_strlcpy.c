size_t	strlcpy(char *dst, char *src, size_t dstsize)
{
	size_t	i;

	i = 0;
	while (dst[i] || i < dstsize)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (i);
}
